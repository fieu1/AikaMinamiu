public class Calculator {
    
    /**
 
* Ini adalah program pencarian data menggunakan binary search
 
* Pada metode binary search, data harus dalam keadaan terurut
 
* Pada program ini data yang diinputkan secara acak, diurutkan terlebih dahulu menggunakan method UrutBil
 
* @author Aika
 
**/
    public double kali(double operandA,double operandB){
        return operandA*operandB;
    }
    
    public double bagi(double operandA,double operandB){
        return operandA/operandB;
    }
    
    public double tambah(double operandA,double operandB){
        return operandA+operandB;
    }
    
    public double kurang(double operandA,double operandB){
        return operandA-operandB;
    }
}
