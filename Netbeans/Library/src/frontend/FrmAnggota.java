    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        Anggota ann = new Anggota();
        ann.setIdanggota(Integer.parseInt(txtIdAnggota.getText()));
        ann.setNama(txtNama.getText());
        ann.setAlamat(txtAlamat.getText());
        ann.setTelepon(txtTelepon.getText());
        ann.save();
        txtIdAnggota.setText(Integer.toString(ann.getIdanggota()));
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblAnggota.getModel();
        int row = tblAnggota.getSelectedRow();

        Anggota ann = new Anggota().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        ann.delete();
        kosongkanForm();
        tampilkanData();
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnTambahBaruActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahBaruActionPerformed
        kosongkanForm();
    }//GEN-LAST:event_btnTambahBaruActionPerformed

